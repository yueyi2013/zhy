        }
        #endregion

        #region 按钮操作
        /// <summary>
        /// 查询操作
        /// </summary>
        /// <param name="sender"></param>
        /// <param name="e"></param>
        protected void btnSearch_Click(object sender, EventArgs e)
        {
            MstGridViewBind();
        }

        /// <summary>
        ///  保存
        /// </summary>
        /// <param name="sender"></param>
        /// <param name="e"></param>
        protected void btnSave_Click(object sender, EventArgs e)
        {
            int rssId = ConvertInt32(hfRSSID.Value, 0);
            if (rssId == 0)
            {
                Add();
                InitDataClear();
            }
            else
            {
                Modify(rssId);
            }
            SelfInform(this.MyUpdatePanelPanelBody, this.GetType(), "保存成功！");
            MstGridViewBind();
        }

        /// <summary>
        /// 删除操作
        /// </summary>
        /// <param name="sender"></param>
        /// <param name="e"></param>
        protected void btnDelete_Click(object sender, EventArgs e)
        {
            foreach (GridViewRow gvr in MstGridView.Rows)
            {
                string rssId = MstGridView.DataKeys[gvr.RowIndex].Value.ToString();
                CheckBox chk = (CheckBox)gvr.FindControl("chkItem");
                if (chk.Checked)
                {
                    ZHY.BLL.RSSSite bll = new ZHY.BLL.RSSSite();
                    bll.Delete(ConvertInt32(rssId, 0));
                }
            }
            MstGridViewBind();

        }

        /// <summary>
        /// 修改按钮触发
        /// </summary>
        /// <param name="sender"></param>
        /// <param name="e"></param>
        protected void btnModify_Click(object sender, EventArgs e)
        {
            foreach (GridViewRow gvr in MstGridView.Rows)
            {
                string rssId = MstGridView.DataKeys[gvr.RowIndex].Value.ToString();
                CheckBox chk = (CheckBox)gvr.FindControl("chkItem");
                if (chk.Checked)
                {
                    InitData(ConvertInt32(rssId, 0));
                    this.mpMstAdd.Show();
                    btnModify.Enabled = true;
                }
            }
        }

        /// <summary>
        ///  关闭
        /// </summary>
        /// <param name="sender"></param>
        /// <param name="e"></param>
        protected void btnClose_Click(object sender, EventArgs e)
        {
            this.mpMstAdd.Hide();
            InitDataClear();
        }

        #endregion

        #region 公共方法

        /// <summary>
        /// 获取登录用户
        /// </summary>
        /// <returns></returns>
        private ZHY.Model.User getLoginUser()
        {
            ZHY.Model.User user = new ZHY.Model.User();
            if (Session["User"] != null)
            {
                user = (ZHY.Model.User)Session["User"];
            }
            else
            {
                user.UserName = "未知用户";
            }
            return user;
        }

        /// <summary>
        /// 新增
        /// </summary>
        private void Add()
        {
            ZHY.Model.RSSSite model = new ZHY.Model.RSSSite();
            model.RSSURL = this.txtRSSUrl.Text;
            model.RSSSource = this.txtSource.Text;
            model.RSSDesc = this.txtDes.Text;
            ZHY.Model.User user = getLoginUser();
            model.NavCreateBy = user.UserName;
            model.NavUpdateBy = user.UserName;
            ZHY.BLL.RSSSite bll = new ZHY.BLL.RSSSite();
            bll.Add(model);
        }

        /// <summary>
        /// 修改
        /// </summary>
        /// <param name="LMID"></param>
        private void Modify(int ID)
        {
            ZHY.BLL.RSSSite bll = new ZHY.BLL.RSSSite();
            ZHY.Model.RSSSite model = bll.GetModel(ID);
            if (model!=null)
            {
                model.RSSURL = this.txtRSSUrl.Text;
                model.RSSSource = this.txtSource.Text;
                model.RSSDesc = this.txtDes.Text;
                ZHY.Model.User user = getLoginUser();
                model.NavUpdateBy = user.UserName;
                model.NavUpdateDT = DateTime.Now;
                bll.Update(model);
            }
        }

        /// <summary>
        /// 初始化清空数据控件
        /// </summary>
        private void InitDataClear()
        {
            this.hfRSSID.Value = "0";
            this.txtRSSUrl.Text = "";
            this.txtDes.Text = "";
            this.txtSource.Text = "";
        }

        /// <summary>
        /// 初始化数据控件
        /// </summary>
        private void InitData(int rssId)
        {
            ZHY.BLL.RSSSite bll = new ZHY.BLL.RSSSite();
            ZHY.Model.RSSSite model = bll.GetModel(rssId);
            this.hfRSSID.Value = model.RSSId.ToString();
            this.txtRSSUrl.Text = model.RSSURL;
            this.txtSource.Text = model.RSSSource;
            this.txtDes.Text = model.RSSDesc;
        }
        #endregion
    }
}